def pil_loader(path):
    # open path as file to avoid ResourceWarning
    # (https://github.com/python-pillow/Pillow/issues/835)
    with open(path, 'rb') as f:
        with Image.open(f) as img:
            return img.convert('RGB')


class MonoDataset(data.Dataset):
    """Superclass for monocular dataloaders

    Args:
        data_path
        filenames
        height
        width
        frame_idxs
        num_scales
        is_train
        img_ext
    """
    def __init__(self,
                 data_path,
                 filenames,
                 height,
                 width,
                 frame_idxs,
                 num_scales,
                 is_train=False,
                 img_ext='.png'):
        super(MonoDataset, self).__init__()

        self.data_path = data_path
        self.filenames = filenames
        self.height = height
        self.width = width
        self.num_scales = num_scales
        self.interp = Image.ANTIALIAS

        self.frame_idxs = frame_idxs

        self.is_train = is_train
        self.img_ext = img_ext

        self.loader = pil_loader
        self.to_tensor = transforms.ToTensor()

        self.frame_range_cache = {}

        # We need to specify augmentations differently in newer versions of torchvision.
        # We first try the newer tuple version; if this fails we fall back to scalars
        try:
            self.brightness = (0.8, 1.2)
            self.contrast = (0.8, 1.2)
            self.saturation = (0.8, 1.2)
            self.hue = (-0.1, 0.1)
            transforms.ColorJitter.get_params(
                self.brightness, self.contrast, self.saturation, self.hue)
        except TypeError:
            self.brightness = 0.2
            self.contrast = 0.2
            self.saturation = 0.2
            self.hue = 0.1

        self.resize = {}
        for i in range(self.num_scales):
            s = 2 ** i
            self.resize[i] = transforms.Resize((self.height // s, self.width // s),
                                               interpolation=self.interp)
        self.load_depth = self.check_depth()

    def custom_get_params(self,
        brightness: Optional[List[float]],
        contrast: Optional[List[float]],
        saturation: Optional[List[float]],
        hue: Optional[List[float]],
    ) -> Tuple[torch.Tensor, Optional[float], Optional[float], Optional[float], Optional[float]]:
        """Get the parameters for the randomized transform to be applied on image.

        Args:
            brightness (tuple of float (min, max), optional): The range from which the brightness_factor is chosen
                uniformly. Pass None to turn off the transformation.
            contrast (tuple of float (min, max), optional): The range from which the contrast_factor is chosen
                uniformly. Pass None to turn off the transformation.
            saturation (tuple of float (min, max), optional): The range from which the saturation_factor is chosen
                uniformly. Pass None to turn off the transformation.
            hue (tuple of float (min, max), optional): The range from which the hue_factor is chosen uniformly.
                Pass None to turn off the transformation.

        Returns:
            tuple: The parameters used to apply the randomized transform
            along with their random order.
        """
        fn_idx = torch.randperm(4)

        b = None if brightness is None else float(torch.empty(1).uniform_(brightness[0], brightness[1]))
        c = None if contrast is None else float(torch.empty(1).uniform_(contrast[0], contrast[1]))
        s = None if saturation is None else float(torch.empty(1).uniform_(saturation[0], saturation[1]))
        h = None if hue is None else float(torch.empty(1).uniform_(hue[0], hue[1]))

        return fn_idx, b, c, s, h
    
    def preprocess(self, inputs, do_color_aug):
        """Resize colour images to the required scales and augment if required

        We create the color_aug object in advance and apply the same augmentation to all
        images in this item. This ensures that all images input to the pose network receive the
        same augmentation.
        """
        for k in list(inputs):
            frame = inputs[k]
            if "color" in k or "inpaint_color" in k:
                n, im, i = k
                for i in range(self.num_scales):
                    inputs[(n, im, i)] = self.resize[i](inputs[(n, im, i - 1)])                
        
        if do_color_aug:
            if torchvision.__version__ == "0.8.2":
                fn_idx, brightness_factor, contrast_factor, saturation_factor, hue_factor = self.custom_get_params(
                    self.brightness, self.contrast, self.saturation, self.hue)
            else:
                fn_idx, brightness_factor, contrast_factor, saturation_factor, hue_factor = transforms.ColorJitter.get_params(
                    self.brightness, self.contrast, self.saturation, self.hue)

        for k in list(inputs):
            f = inputs[k]
            if "color" in k or "inpaint_color" in k:
                n, im, i = k
                inputs[(n, im, i)] = self.to_tensor(f)
                if do_color_aug and "color" in k:
                    for fn_id in fn_idx:
                        if fn_id == 0 and brightness_factor is not None:
                            f = F.adjust_brightness(f, brightness_factor)
                        elif fn_id == 1 and contrast_factor is not None:
                            f = F.adjust_contrast(f, contrast_factor)
                        elif fn_id == 2 and saturation_factor is not None:
                            f = F.adjust_saturation(f, saturation_factor)
                        elif fn_id == 3 and hue_factor is not None:
                            f = F.adjust_hue(f, hue_factor)
                inputs[(n + "_aug", im, i)] = self.to_tensor(f)

    def __len__(self):
        return len(self.filenames)

    def __getitem__(self, index):
        """Returns a single training item from the dataset as a dictionary.

        Values correspond to torch tensors.
        Keys in the dictionary are either strings or tuples:

            ("color", <frame_id>, <scale>)          for raw colour images,
            ("color_aug", <frame_id>, <scale>)      for augmented colour images,
            ("K", scale) or ("inv_K", scale)        for camera intrinsics,
            "stereo_T"                              for camera extrinsics, and
            "depth_gt"                              for ground truth depth maps.

        <frame_id> is either:
            an integer (e.g. 0, -1, or 1) representing the temporal step relative to 'index',
        or
            "s" for the opposite image in the stereo pair.

        <scale> is an integer representing the scale of the image relative to the fullsize image:
            -1      images at native resolution as loaded from disk
            0       images resized to (self.width,      self.height     )
            1       images resized to (self.width // 2, self.height // 2)
            2       images resized to (self.width // 4, self.height // 4)
            3       images resized to (self.width // 8, self.height // 8)
        """
        inputs = {}

        do_color_aug = self.is_train and random.random() > 0.5
        do_flip = self.is_train and random.random() > 0.5
        do_rot = self.is_train and random.random() > 0.5

        line = self.filenames[index].split()
        if len(line) == 1:
            if self.data_path not in line[0]:
                folder = os.path.join(self.data_path,os.path.dirname(line[0]))
            else:
                folder = os.path.dirname(line[0])
                
            # Extract the filename from the path
            filename = os.path.basename(line[0]).split(".")[0]

            # Use regular expression to find the first sequence of digits
            match = re.search(r'\d+', filename)

            # Convert the found sequence to an integer
            if match:
                frame_index = int(match.group())
            else:
                raise ValueError(f"No digits found in filename: {filename}")

            side = None
        else:
            folder = line[0]

            if len(line) == 3:
                frame_index = int(line[1])
            else:
                frame_index = 0

            if len(line) == 3:
                side = line[2]
            else:
                side = None

        #check frame boundary
        if folder not in self.frame_range_cache:
            frame_files = [
                f for f in os.listdir(folder)
                if f.endswith("_color" + self.img_ext)
            ]

            indices = sorted([
                int(re.search(r'\d+', f).group())
                for f in frame_files
            ])

            self.frame_range_cache[folder] = (indices[0], indices[-1])

        min_idx, max_idx = self.frame_range_cache[folder]

        min_allowed = min_idx - min(self.frame_idxs)
        max_allowed = max_idx - max(self.frame_idxs)

        if frame_index < min_allowed:
            frame_index = min_allowed
        elif frame_index > max_allowed:
            frame_index = max_allowed

        for i in self.frame_idxs:
            if isinstance(self.inpaint_pseudo_gt_dir, list):
                if "BBPS-2-3Frames" in folder:
                    inpaint_pseudo_gt_dir = self.inpaint_pseudo_gt_dir[0]
                    data_path = self.data_path[0]
                elif "C3VD" in folder:
                    inpaint_pseudo_gt_dir = self.inpaint_pseudo_gt_dir[1]
                    data_path = self.data_path[1]
            else:
                inpaint_pseudo_gt_dir = self.inpaint_pseudo_gt_dir
                data_path = self.data_path
            if i == "s":
                other_side = {"r": "l", "l": "r"}[side]
                inputs[("color", i, -1)] = self.get_color(folder, frame_index, other_side, do_flip, do_rot)
                if self.inpaint_pseudo_gt_dir is not None:
                    inpainted_folder = folder.replace(data_path, inpaint_pseudo_gt_dir)
                    inputs[("inpaint_color", i, -1)] = self.get_color(inpainted_folder, frame_index, other_side, do_flip, do_rot)
            else:
                inputs[("color", i, -1)] = self.get_color(folder, frame_index + i, side, do_flip, do_rot)
                if self.inpaint_pseudo_gt_dir is not None:
                    inpainted_folder = folder.replace(data_path, inpaint_pseudo_gt_dir)
                    inputs[("inpaint_color", i, -1)] = self.get_color(inpainted_folder, frame_index + i, side, do_flip, do_rot)

        # adjusting intrinsics to match each scale in the pyramid
        for scale in range(self.num_scales):
            K = self.K.copy()
            K[0, :] *= self.width // (2 ** scale)
            K[1, :] *= self.height // (2 ** scale)


            inv_K = np.linalg.pinv(K)

            inputs[("K", scale)] = torch.from_numpy(K)
            inputs[("inv_K", scale)] = torch.from_numpy(inv_K)

        self.preprocess(inputs, do_color_aug)
        for i in self.frame_idxs:
            del inputs[("color", i, -1)]
            del inputs[("color_aug", i, -1)]
            if self.inpaint_pseudo_gt_dir is not None:
                del inputs[("inpaint_color", i, -1)]

        if self.load_depth:
            depth_gt = self.get_depth(folder, frame_index, side, do_flip)
            inputs["depth_gt"] = np.expand_dims(depth_gt, 0)
            inputs["depth_gt"] = torch.from_numpy(inputs["depth_gt"].astype(np.float32))

        if "s" in self.frame_idxs:
            stereo_T = np.eye(4, dtype=np.float32)
            baseline_sign = -1 if do_flip else 1
            side_sign = -1 if side == "l" else 1
            stereo_T[0, 3] = side_sign * baseline_sign * 0.1

            inputs["stereo_T"] = torch.from_numpy(stereo_T)
        return inputs

    def get_color(self, folder, frame_index, side, do_flip):
        raise NotImplementedError

    def check_depth(self):
        raise NotImplementedError

    def get_depth(self, folder, frame_index, side, do_flip):
        raise NotImplementedError
